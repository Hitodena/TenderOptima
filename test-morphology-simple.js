// Функция нормализации русских слов (копия из matching-service.ts)
function normalizeRussianWord(word) {
    const normalizedWord = word.toLowerCase().trim();
    
    if (normalizedWord.length < 3) {
        return [normalizedWord];
    }
    
    // Словарь морфологических преобразований
    const morphologyMap = {
        // Перчатки - различные формы + опечатка "перчаки" 
        'перчатка': ['перчатка', 'перчатки', 'перчаток', 'перчатке', 'перчатку', 'перчаткой', 'перчаткам', 'перчатками', 'перчатках', 'перчаки'],
        'перчатки': ['перчатка', 'перчатки', 'перчаток', 'перчатке', 'перчатку', 'перчаткой', 'перчаткам', 'перчатками', 'перчатках', 'перчаки'],
        'перчаки': ['перчатка', 'перчатки', 'перчаток', 'перчатке', 'перчатку', 'перчаткой', 'перчаткам', 'перчатками', 'перчатках', 'перчаки'],
        
        // Коробка - различные формы  
        'коробка': ['коробка', 'коробки', 'коробке', 'коробку', 'коробкой', 'коробкам', 'коробками', 'коробках'],
        'коробки': ['коробка', 'коробки', 'коробке', 'коробку', 'коробкой', 'коробкам', 'коробками', 'коробках'],
        
        // Материал - различные формы
        'материал': ['материал', 'материала', 'материалу', 'материалом', 'материале', 'материалы', 'материалов', 'материалам', 'материалами', 'материалах'],
        'материалы': ['материал', 'материала', 'материалу', 'материалом', 'материале', 'материалы', 'материалов', 'материалам', 'материалами', 'материалах']
    };
    
    // Если есть точное соответствие в словаре
    if (morphologyMap[normalizedWord]) {
        return morphologyMap[normalizedWord];
    }
    
    // Если нет точного соответствия, попробуем найти по основе
    for (const [baseForm, forms] of Object.entries(morphologyMap)) {
        if (forms.includes(normalizedWord)) {
            return forms;
        }
    }
    
    return [normalizedWord];
}

// Тестируем функцию
console.log('\n=== РЕЗУЛЬТАТЫ ТЕСТИРОВАНИЯ ===\n');

const testCases = [
    {
        input: 'перчатка',
        expected: 'должен найти "перчатки"',
        context: 'поставщик с категорией "перчатки"'
    },
    {
        input: 'перчаки',
        expected: 'должен найти "перчатки" (исправление опечатки)',
        context: 'поставщик с категорией "перчатки"'
    },
    {
        input: 'коробка',
        expected: 'должен найти "коробки"', 
        context: 'поставщик с категорией "коробки"'
    },
    {
        input: 'материал',
        expected: 'должен найти "материалы"',
        context: 'поставщик с категорией "материалы"'
    }
];

testCases.forEach((test, index) => {
    console.log(`${index + 1}. Тест: поиск "${test.input}"`);
    
    const variations = normalizeRussianWord(test.input);
    console.log(`   Сгенерированные варианты: ${variations.join(', ')}`);
    
    // Симулируем поиск в тексте поставщика
    const supplierText = test.context.toLowerCase();
    console.log(`   Текст поставщика: "${supplierText}"`);
    
    // Добавим отладочную информацию для "перчаки"
    if (test.input === 'перчаки') {
        console.log(`   === ОТЛАДКА для "перчаки" ===`);
        console.log(`   Нормализованное слово: "${test.input.toLowerCase()}"`);
        console.log(`   Поиск "перчатки" в тексте: ${supplierText.includes('перчатки')}`);
        console.log(`   Вариации включают "перчатки": ${variations.includes('перчатки')}`);
    }
    
    let found = false;
    variations.forEach(variation => {
        if (supplierText.includes(variation)) {
            console.log(`   ✅ НАЙДЕНО совпадение: "${variation}"`);
            found = true;
        }
    });
    
    if (found) {
        console.log(`   ✅ ТЕСТ ПРОЙДЕН: ${test.expected}`);
    } else {
        console.log(`   ❌ ТЕСТ НЕ ПРОЙДЕН: совпадений не найдено`);
    }
    
    console.log('');
});

