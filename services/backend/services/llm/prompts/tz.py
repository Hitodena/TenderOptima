def build_comparison_chunk_prompt(
    tz_chunk: list[str],
    kp_name: str,
    kp_flat: list[str],
    *,
    chunk_idx: int,
    total_chunks: int,
) -> tuple[str, str]:
    tz_list = "\n".join(f"{i + 1}. {req}" for i, req in enumerate(tz_chunk))
    kp_list = "\n".join(f"{i + 1}. {off}" for i, off in enumerate(kp_flat))

    first_num = tz_chunk[0].split(".", 1)[0] if tz_chunk else "?"
    last_num = tz_chunk[-1].split(".", 1)[0] if tz_chunk else "?"

    system = f"""\
Ты — специалист по анализу тендерных предложений. Сопоставь требования \
заказчика (ТЗ) с тем, что предлагает поставщик (КП).

ЧАНК {chunk_idx + 1} из {total_chunks} (требования {first_num} — {last_num}).

## Статусы

- "met"        — КП явно закрывает требование
- "partial"    — КП упоминает, но неполно или с оговорками
- "missing"    — в КП есть ответ, но он не соответствует
- "not_found"  — в КП нет информации по требованию

## Правила

1. Одно требование ТЗ = один item
2. Ищи в КП предложения поставщика (товар, характеристики, срок, цена), \
не процедурный текст
3. Если требование ТЗ — процедурное (подача предложений, язык, обжалование) \
и в КП нет ответа — status: "not_found", explanation: процедурное требование
4. Технические эквиваленты RU/EN:
{_TECH_EQUIVALENTS}
5. Числовые диапазоны: значение в диапазоне → met; «≥600» при «≥400» → met

Верни ТОЛЬКО JSON:
{{
  "items": [
    {{
      "requirement": "цитата из ТЗ",
      "requirement_ref": "ТЗ, п. X: «...»",
      "offer_value": "цитата из КП или null",
      "offer_ref": "КП, п. Y: «...» или null",
      "explanation": "обоснование",
      "status": "met|partial|missing|not_found"
    }}
  ]
}}

Проанализируй РОВНО {len(tz_chunk)} требований из списка: \
не добавляй лишних и не пропускай ни одного."""

    user = (
        f"### Требования из ТЗ (чанк {chunk_idx + 1}/{total_chunks}):\n"
        f"{tz_list}\n\n"
        f"### Предложения из КП ({kp_name}):\n{kp_list}"
    )
    return system, user
