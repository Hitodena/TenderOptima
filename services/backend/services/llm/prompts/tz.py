def build_tz_extract_prompt(tz_text: str) -> tuple[str, str]:
    system = """\
Ты — аналитик тендерной документации. Извлеки из технического задания (ТЗ) \
список всех требований к поставщику.

Правила:
1. Каждое требование — отдельный пункт списка
2. Не объединяй несколько требований в одно
3. Формулировки — дословные цитаты из ТЗ, не перефразируй
4. Включай технические характеристики, условия поставки, сроки, гарантии и т.д.
5. Пропускай только оглавление, шапки таблиц без содержания, подписи

Верни ТОЛЬКО JSON без markdown-обёрток:
{
  "requirements": [
    "дословная цитата требования 1",
    "дословная цитата требования 2"
  ]
}"""

    user = f"### ТЗ:\n{tz_text}"
    return system, user


def build_kp_extract_prompt(kp_text: str) -> tuple[str, str]:
    system = """\
Ты — аналитик тендерной документации. Извлеки из коммерческого предложения (КП) \
список всех предложений, характеристик и условий поставщика.

Правила:
1. Каждое предложение — отдельный пункт списка
2. Не объединяй несколько предложений в одно
3. Формулировки — дословные цитаты из КП, не перефразируй
4. Включай цены, характеристики, сроки, условия, гарантии и т.д.
5. Пропускай только оглавление, шапки таблиц без содержания, подписи

Верни ТОЛЬКО JSON без markdown-обёрток:
{
  "offerings": [
    "дословная цитата предложения 1",
    "дословная цитата предложения 2"
  ]
}"""

    user = f"### КП поставщика:\n{kp_text}"
    return system, user


def build_comparison_prompt(
    requirements_tz: list[str],
    kp_name: str,
    kp_offerings: list[str],
) -> tuple[str, str]:
    tz_list = "\n".join(
        f"{i + 1}. {req}" for i, req in enumerate(requirements_tz)
    )
    kp_list = "\n".join(
        f"{i + 1}. {off}" for i, off in enumerate(kp_offerings)
    )

    system = """\
Ты — аналитик тендерной документации. Сопоставь каждое требование из ТЗ \
с предложениями из КП поставщика.

Для каждого требования из ТЗ определи статус:
- "met"        — КП явно и полностью закрывает требование
- "partial"    — КП упоминает требование, но с оговорками, неточностями или неполно
- "missing"    — требование в КП упомянуто, но не закрыто (отклонение, неполнота)
- "not_found"  — в КП нет упоминания этого требования

Правила:
1. Сопоставляй КАЖДОЕ требование из списка ТЗ — по одному item на каждое
2. В "requirement" — дословная цитата из списка требований ТЗ
3. В "requirement_ref" — номер пункта из списка ТЗ, например: ТЗ, п. 3: «цитата»
4. В "offer_value" — дословная цитата из списка предложений КП (или null)
5. В "offer_ref" — номер пункта из списка КП (или null), например: КП, п. 2: «цитата»
6. В "explanation" — краткое объяснение сопоставления для ВСЕХ статусов
7. Для "met" тоже заполняй offer_value, offer_ref и explanation
8. Сопоставляй по смыслу: что именно требует ТЗ и что ответил поставщик в КП

Верни ТОЛЬКО JSON без markdown-обёрток:
{
  "items": [
    {
      "requirement": "дословная цитата из ТЗ",
      "requirement_ref": "ТЗ, п. X: «цитата из ТЗ»",
      "offer_value": "дословная цитата из КП или null",
      "offer_ref": "КП, п. Y: «цитата из КП» или null",
      "explanation": "объяснение анализа",
      "status": "met|partial|missing|not_found"
    }
  ]
}"""

    user = (
        f"### Требования из ТЗ:\n{tz_list}\n\n"
        f"### Предложения из КП ({kp_name}):\n{kp_list}"
    )
    return system, user
