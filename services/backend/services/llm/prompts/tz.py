def _chunk_user_prompt(
    *,
    doc_heading: str,
    chunk: str,
    rolling_context: str | None,
    chunk_idx: int,
    total_chunks: int,
) -> str:
    parts: list[str] = []
    if rolling_context:
        parts.append(rolling_context)
        parts.append(
            "Продолжи извлечение. Не повторяй уже извлечённые номера; "
            "извлекай только новые пункты текущего фрагмента."
        )
    elif chunk_idx == 0:
        parts.append(
            "Первый фрагмент. Извлекай только содержательные пункты; "
            "не включай пустые заголовки разделов."
        )
    parts.append(
        f"Фрагмент {chunk_idx + 1} из {total_chunks}.\n"
        f"### {doc_heading}:\n{chunk}"
    )
    return "\n\n".join(parts)


def build_tz_chunk_prompt(
    chunk: str,
    rolling_context: str | None,
    chunk_idx: int,
    total_chunks: int,
) -> tuple[str, str]:
    return (
        _tz_extraction_system_prompt(),
        _chunk_user_prompt(
            doc_heading="ТЗ / документация на закупку",
            chunk=chunk,
            rolling_context=rolling_context,
            chunk_idx=chunk_idx,
            total_chunks=total_chunks,
        ),
    )


