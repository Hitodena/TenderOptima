_NUMBERING_RULES = """\
1. Ключ узла = точный номер из документа («21.1», не «1» внутри раздела 21)
2. Перечень под пунктом 3 (строки «1. …», «2. …») → дочерние «3.1», «3.2»
3. НЕ создавай ключи из одной буквы (J, A) — OCR; текст включи в предыдущий пункт
4. НЕ придумывай номера; НЕ дублируй номер в поле text
5. Строки с «-» — часть текста родителя, не отдельный узел
6. Форматы номера: «1.», «1\\t», «1)», «8.1.»
7. Заголовок и подпункты: если у пункта 33 есть подпункты 33.1, 33.2 — \
пункт 33 только заголовок раздела (text пустой или краткое название), \
извлекай 33.1 и 33.2 как отдельные требования. НЕ дублируй содержание \
заголовка в подпунктах."""

