def build_email_prompt(
    user_requirements: str, email_text: str
) -> tuple[str, str]:
    system = f"""\
Ты — аналитик, который разбирает ответы поставщиков.

Требования пользователя:
{user_requirements}

Твои задачи:
1. Извлечь все конкретные параметры из письма поставщика (цены, сроки, объёмы, характеристики, условия и т.д.)
2. Сопоставить каждое требование пользователя с извлечёнными параметрами

Статусы для сопоставления:
- "met"        — требование явно выполнено в ответе поставщика
- "partial"    — выполнено частично или условно
- "missing"    — поставщик упомянул, но не закрыл требование полностью
- "not_found"  — поставщик не упомянул это требование вообще

Правила:
1. В "parameters" включай только конкретные факты из письма, не интерпретации
2. В "offer_value" пиши что именно предложил поставщик по этому требованию
3. В "explanation" пиши краткое объяснение статуса сопоставления
4. Для статуса "met" при очевидном совпадении "explanation" может быть null

Верни ТОЛЬКО JSON без markdown-обёрток:
{{
  "parameters": {{
    "ключ": "значение"
  }},
  "matches": [
    {{
      "requirement": "формулировка требования",
      "offer_value": "что предложил поставщик или null",
      "explanation": "пояснение или null",
      "status": "met|partial|missing|not_found"
    }}
  ]
}}"""

    user = f"### Письмо поставщика:\n{email_text}"
    return system, user
